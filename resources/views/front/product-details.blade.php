<script>
    const canvas = document.getElementById('c');
    const ctx = canvas.getContext('2d');
    let w, h, midX, midY;

    // --- SLOWED DOWN CONFIGURATION ---
    const opts = {
        radius: 220,             
        nodeBaseSize: 3.5,         
        lineOpacity: 0.12,       
        glowWidth: 2.2,          
        pulseSpeed: 0.008,       // Very calm movement
        pulseCount: 5,           
        rotVelX: 0.0004,         // Slow ambient drift
        rotVelY: 0.0008,         
        depth: 450,              
        focalLength: 350,        
        repaintColor: 'rgba(255, 255, 255, 0.95)', 
        nodeColor: 'hsla(210, 80%, 50%, 0.6)', 
        wireColor: 'rgba(120, 160, 240, 0.12)', 
    };

    let cosX = 1, sinX = 0, cosY = 1, sinY = 0;
    let mostDistant;
    let allNodes = [], allEdges = [], allData = [];
    let tick = 0;
    
    // Fixed spin velocity for auto-play only
    let spinVel = { x: opts.rotVelX, y: opts.rotVelY };

    function generateGeodesic(subdivisions) {
        allNodes = []; allEdges = []; allData = [];
        const phi = (1 + Math.sqrt(5)) / 2;
        const v = [[-1,phi,0],[1,phi,0],[-1,-phi,0],[1,-phi,0],[0,-1,phi],[0,1,phi],[0,-1,-phi],[0,1,-phi],[phi,0,-1],[phi,0,1],[-phi,0,-1],[-phi,0,1]];
        const f = [[0,11,5],[0,5,1],[0,1,7],[0,7,10],[0,10,11],[1,5,9],[5,11,4],[11,10,2],[10,7,6],[7,1,8],[3,9,4],[3,4,2],[3,2,6],[3,6,8],[3,8,9],[4,9,5],[2,4,11],[6,2,10],[8,6,7],[9,8,1]];

        function createNode(vArr) {
            const mag = Math.sqrt(vArr[0]**2 + vArr[1]**2 + vArr[2]**2);
            const x = vArr[0] / mag * opts.radius;
            const y = vArr[1] / mag * opts.radius;
            const z = vArr[2] / mag * opts.radius;
            const existing = allNodes.find(n => n.orig.x === x && n.orig.y === y && n.orig.z === z);
            if(existing) return existing;
            const node = new Node(x, y, z, opts.nodeBaseSize);
            allNodes.push(node);
            return node;
        }

        function createEdge(n1, n2) {
            if(!allEdges.find(e => (e.n1 === n1 && e.n2 === n2) || (e.n1 === n2 && e.n2 === n1))) {
                const edge = new Edge(n1, n2);
                allEdges.push(edge);
                n1.edges.push(edge);
                n2.edges.push(edge);
            }
        }

        function subdivide(v1, v2, v3, depth) {
            if (depth === 0) {
                const n1 = createNode(v1); const n2 = createNode(v2); const n3 = createNode(v3);
                createEdge(n1, n2); createEdge(n2, n3); createEdge(n3, n1);
                return;
            }
            const m1 = v1.map((c, i) => (c + v2[i]) / 2);
            const m2 = v2.map((c, i) => (c + v3[i]) / 2);
            const m3 = v3.map((c, i) => (c + v1[i]) / 2);
            subdivide(v1, m1, m3, depth - 1); subdivide(v2, m2, m1, depth - 1); subdivide(v3, m3, m2, depth - 1); subdivide(m1, m2, m3, depth - 1);
        }

        for (let i = 0; i < f.length; i++) subdivide(v[f[i][0]], v[f[i][1]], v[f[i][2]], subdivisions);
    }

    class Node {
        constructor(x, y, z, size) { this.orig = { x, y, z }; this.x = x; this.y = y; this.z = z; this.size = size; this.edges = []; this.screen = {}; }
        project() {
            let Y = this.y * cosX - this.z * sinX;
            let Z = this.z * cosX + this.y * sinX;
            let X = this.x * cosY + Z * sinY;
            Z = Z * cosY - this.x * sinY;
            this.screen.z = Z;
            let sc = opts.focalLength / (Z + opts.depth);
            this.screen.x = midX + X * sc; this.screen.y = midY + Y * sc; this.screen.scale = sc;
        }
        draw() {
            const alpha = 0.15 + (1 - this.screen.z / mostDistant) * 0.7;
            ctx.fillStyle = opts.nodeColor.replace('0.6', alpha);
            ctx.beginPath(); ctx.arc(this.screen.x, this.screen.y, this.screen.scale * this.size, 0, Math.PI * 2); ctx.fill();
        }
    }

    class Edge {
        constructor(n1, n2) { this.n1 = n1; this.n2 = n2; }
        getNeighbor(node) { return node === this.n1 ? this.n2 : this.n1; }
        drawWire() { ctx.moveTo(this.n1.screen.x, this.n1.screen.y); ctx.lineTo(this.n2.screen.x, this.n2.screen.y); }
    }

    class DataPulse {
        constructor() {
            this.node = allNodes[Math.random() * allNodes.length | 0];
            this.proportion = 0;
            this.targetEdge = this.node.edges[Math.random() * this.node.edges.length | 0];
            this.targetNode = this.targetEdge.getNeighbor(this.node);
            this.screen = {};
        }
        step() {
            this.proportion += opts.pulseSpeed;
            if (this.proportion >= 1) {
                this.node = this.targetNode;
                this.targetEdge = this.node.edges[Math.random() * this.node.edges.length | 0];
                this.targetNode = this.targetEdge.getNeighbor(this.node);
                this.proportion = 0;
            }
            this.x = this.node.x + (this.targetNode.x - this.node.x) * this.proportion;
            this.y = this.node.y + (this.targetNode.y - this.node.y) * this.proportion;
            this.z = this.node.z + (this.targetNode.z - this.node.z) * this.proportion;
            let Y = this.y * cosX - this.z * sinX;
            let Z = this.z * cosX + this.y * sinX;
            let X = this.x * cosY + Z * sinY;
            Z = Z * cosY - this.x * sinY;
            this.screen.x = midX + X * (opts.focalLength / (Z + opts.depth));
            this.screen.y = midY + Y * (opts.focalLength / (Z + opts.depth));
            this.screen.z = Z;
            this.screen.scale = opts.focalLength / (Z + opts.depth);
        }
        drawGlow() {
            const alpha = 0.3 + (1 - this.screen.z / mostDistant) * 0.6;
            const size = opts.glowWidth * this.screen.scale;
            const gradSize = size * 3;
            const grad = ctx.createRadialGradient(this.screen.x, this.screen.y, size/2, this.screen.x, this.screen.y, gradSize);
            const hue = (tick * 0.5) % 360; 
            grad.addColorStop(0, `hsla(${hue}, 100%, 75%, ${alpha})`);
            grad.addColorStop(0.5, `hsla(${hue + 40}, 100%, 60%, ${alpha * 0.5})`);
            grad.addColorStop(1, `hsla(${hue + 40}, 100%, 50%, 0)`);
            ctx.fillStyle = grad;
            ctx.beginPath(); ctx.arc(this.screen.x, this.screen.y, gradSize, 0, Math.PI * 2); ctx.fill();
        }
    }

    function anim() {
        window.requestAnimationFrame(anim);
        ctx.globalCompositeOperation = 'source-over';
        ctx.fillStyle = opts.repaintColor;
        ctx.fillRect(0, 0, w, h);
        tick++;
        
        // Removed dynamic spin calculation based on mouse interaction
        cosX = Math.cos(tick * spinVel.x);
        sinX = Math.sin(tick * spinVel.x);
        cosY = Math.cos(tick * spinVel.y);
        sinY = Math.sin(tick * spinVel.y);
        
        allNodes.forEach(n => n.project());
        allData.forEach(d => d.step());
        
        ctx.lineWidth = 0.8;
        ctx.strokeStyle = opts.wireColor;
        ctx.beginPath(); allEdges.forEach(e => e.drawWire()); ctx.stroke();
        allNodes.sort((a, b) => b.screen.z - a.screen.z).forEach(n => n.draw());
        ctx.globalCompositeOperation = 'lighter';
        allData.sort((a, b) => b.screen.z - a.screen.z).forEach(d => d.drawGlow());
    }

    function init() {
        w = canvas.width = window.innerWidth; h = canvas.height = window.innerHeight;
        midX = w / 2; midY = h / 2;
        mostDistant = opts.depth + opts.radius;
        generateGeodesic(2);
        allData = [];
        for (let i = 0; i < opts.pulseCount; i++) allData.push(new DataPulse());
        if (!animating) { animating = true; anim(); }
    }
    let animating = false;
    init();

    window.addEventListener('resize', init);
    // Removed all mouse/touch event listeners
</script>

